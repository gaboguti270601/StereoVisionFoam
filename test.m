function coincidencia = buscarFrameMasCercano( ...
    registro, timestampObjetivo)

    diferencias = abs(seconds( ...
        registro.timestamp - timestampObjetivo ...
    ));

    [diferenciaMin, idx] = min(diferencias);

    frameGrabado = double(registro.frame_grabado(idx));

    coincidencia.timestamp_encontrado = registro.timestamp(idx);
    coincidencia.diferencia_s = diferenciaMin;
    coincidencia.frame_grabado_csv = frameGrabado;
    coincidencia.frame_video_matlab = frameGrabado + 1;
end


function [frameLout, frameRout] = adaptarResolucionCalibracion( ...
    frameL, frameR, tamCal)

    if ~isequal(size(frameL, 1:2), tamCal)
        frameLout = imresize(frameL, tamCal);
    else
        frameLout = frameL;
    end

    if ~isequal(size(frameR, 1:2), tamCal)
        frameRout = imresize(frameR, tamCal);
    else
        frameRout = frameR;
    end
end


function desplazamientoR = seleccionarDesplazamientoHorizontal( ...
    rectL, rectR)

    f1 = figure('Name', 'Punto cámara izquierda');
    imshow(rectL);
    title('Selecciona un detalle en la cámara izquierda');
    [xL, yL] = ginput(1);
    close(f1);

    f2 = figure('Name', 'Punto cámara derecha');
    imshow(rectR);
    title('Selecciona el mismo detalle en la cámara derecha');
    [xR, yR] = ginput(1);
    close(f2);

    desplazamientoR = xL - xR;

    fprintf('\nAlineación manual:\n');
    fprintf('xL=%.3f, yL=%.3f\n', xL, yL);
    fprintf('xR=%.3f, yR=%.3f\n', xR, yR);
    fprintf('Diferencia vertical informativa: %.3f px\n', abs(yL-yR));
    fprintf('Desplazamiento horizontal: %.3f px\n', desplazamientoR);
end


