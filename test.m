end


function ROI = seleccionarROI(imagen)
    f = figure('Name', 'Seleccionar ROI de espuma');
    imshow(imagen);
    title('Selecciona la ROI de la superficie/espuma');
    h = drawrectangle('Color', 'yellow');
    wait(h);
    ROI = round(h.Position);
    close(f);

    ROI(1) = max(1, ROI(1));
    ROI(2) = max(1, ROI(2));
    ROI(3) = max(1, ROI(3));
    ROI(4) = max(1, ROI(4));

    ROI(3) = min(ROI(3), size(imagen,2)-ROI(1)+1);
    ROI(4) = min(ROI(4), size(imagen,1)-ROI(2)+1);
end


function [dMediana, zMediana, nValidos, diagnostico] = ...
    calcularFrame( ...
        rectL, ...
        rectR, ...
        stereoParams, ...
        ROI, ...
        minD, ...
        maxD, ...
        clipLimit, ...
        numTiles, ...
        bilateralDegree, ...
        bilateralSigma, ...
        cannyBajo, ...
        cannyAlto, ...
        intensidadMin, ...
        intensidadMax, ...
        minPixeles, ...
        iqrFactor)

    grayOriginalL = im2gray(rectL);
    grayOriginalR = im2gray(rectR);

    claheL = adapthisteq( ...
        grayOriginalL, ...
        'ClipLimit', ...
        clipLimit, ...
        'NumTiles', ...
        numTiles ...
    );

    claheR = adapthisteq( ...
        grayOriginalR, ...
        'ClipLimit', ...
        clipLimit, ...
        'NumTiles', ...
        numTiles ...
    );

    claheL = imbilatfilt( ...
        claheL, ...
        bilateralDegree, ...
        bilateralSigma ...
    );

    claheR = imbilatfilt( ...
        claheR, ...
        bilateralDegree, ...
        bilateralSigma ...
    );

    bordesL = edge( ...
        claheL, ...
        'Canny', ...
        [cannyBajo cannyAlto] ...
    );

    bordesR = edge( ...
        claheR, ...
        'Canny', ...
        [cannyBajo cannyAlto] ...
    );

    disparityMap = disparitySGM( ...
        claheL, ...
        claheR, ...
        'DisparityRange', ...
        [minD maxD], ...
        'UniquenessThreshold', ...
        10 ...
    );

    intensidadValida = ...
        grayOriginalL >= intensidadMin & ...
        grayOriginalL <= intensidadMax & ...
        grayOriginalR >= intensidadMin & ...
        grayOriginalR <= intensidadMax;

    invalidos = ...
        ~isfinite(disparityMap) | ...
        disparityMap <= minD + 2 | ...
        disparityMap >= maxD - 2 | ...
        ~intensidadValida;

    disparityMap(invalidos) = NaN;

    xyz = reconstructScene( ...
        disparityMap, ...
        stereoParams ...
    );

    x = ROI(1);
    y = ROI(2);
    w = ROI(3);
    h = ROI(4);

    x2 = min(size(disparityMap,2), x+w-1);
    y2 = min(size(disparityMap,1), y+h-1);

    roiD = disparityMap(y:y2, x:x2);
    roiZ = xyz(y:y2, x:x2, 3);

    validos = isfinite(roiD) & ...
              isfinite(roiZ) & ...
              roiZ > 0;

    valoresD = double(roiD(validos));
    valoresZ = double(roiZ(validos));

    nValidos = numel(valoresZ);

    if nValidos >= minPixeles
        mascara = mascaraIQR( ...
            valoresZ, ...
            iqrFactor ...
        );

        valoresD = valoresD(mascara);
        valoresZ = valoresZ(mascara);
        nValidos = numel(valoresZ);
    end

    if nValidos < minPixeles
        dMediana = NaN;
        zMediana = NaN;
    else
        dMediana = median(valoresD, 'omitnan');
        zMediana = median(valoresZ, 'omitnan');
    end

    claheMontaje = imtile( ...
        {claheL, claheR}, ...
        'GridSize', ...
        [1 2] ...
    );

    bordesMontaje = imtile( ...
        {bordesL, bordesR}, ...
        'GridSize', ...
        [1 2] ...
    );

    disparidadColor = colorearDisparidad( ...
        disparityMap, ...
        minD, ...
        maxD ...
    );

    disparidadColor = dibujarROI( ...
        disparidadColor, ...
        ROI ...
    );

    diagnosticoImagen = crearDiagnostico( ...
        rectL, ...
        claheL, ...
        bordesL, ...
        disparidadColor, ...
        ROI ...
    );

    diagnostico.claheMontaje = claheMontaje;
    diagnostico.bordesMontaje = bordesMontaje;
    diagnostico.disparidadColor = disparidadColor;
    diagnostico.diagnostico = diagnosticoImagen;
end


function mascara = mascaraIQR(valores, factor)
    q1 = prctile(valores, 25);
    q3 = prctile(valores, 75);
    rango = q3 - q1;

    if rango <= 0
        mascara = true(size(valores));
        return;
    end

    mascara = valores >= q1-factor*rango & ...
              valores <= q3+factor*rango;
end


function salida = colorearDisparidad(dispMap, minD, maxD)
    normalizada = (dispMap - minD) / (maxD - minD);
    normalizada = max(0, min(1, normalizada));
    normalizada(~isfinite(normalizada)) = 0;

    indice = uint8(round(normalizada * 255));
    mapa = turbo(256);

    salida = ind2rgb(indice + 1, mapa);
    salida = im2uint8(salida);
end


function salida = dibujarROI(imagen, ROI)
    salida = imagen;

    if size(salida,3) == 1
        salida = repmat(salida, [1 1 3]);
    end

    salida = insertShape( ...
        salida, ...
        'Rectangle', ...
        ROI, ...
        'Color', ...
        'yellow', ...
        'LineWidth', ...
        3 ...
    );
end


function diagnostico = crearDiagnostico( ...
    rectL, claheL, bordesL, dispColor, ROI)

    rectMarcada = dibujarROI(rectL, ROI);

    claheRGB = repmat(claheL, [1 1 3]);
    claheRGB = dibujarROI(claheRGB, ROI);

    bordesRGB = repmat(uint8(bordesL)*255, [1 1 3]);
    bordesRGB = dibujarROI(bordesRGB, ROI);

    objetivo = [480 640];

    a = imresize(rectMarcada, objetivo);
    b = imresize(claheRGB, objetivo);
    c = imresize(bordesRGB, objetivo);
    d = imresize(dispColor, objetivo);

    diagnostico = [a b; c d];
end


function writer = crearWriter(ruta, fps, calidad)
    writer = VideoWriter(ruta, 'MPEG-4');
    writer.FrameRate = fps;

    if isprop(writer, 'Quality')
        writer.Quality = calidad;
    end

    open(writer);
end


function frame = prepararVideo(frame)
    if islogical(frame)
        frame = uint8(frame) * 255;
    elseif isa(frame, 'double') || isa(frame, 'single')
        frame = im2uint8(frame);
    end

    if size(frame,3) == 1
        frame = repmat(frame, [1 1 3]);
    end

    if mod(size(frame,1),2) ~= 0
        frame = frame(1:end-1,:,:);
    end

    if mod(size(frame,2),2) ~= 0
        frame = frame(:,1:end-1,:);
    end
end
