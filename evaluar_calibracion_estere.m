function angulos = matrizRotacionAAngulos(R)

    valor = sqrt( ...
        R(1, 1)^2 + ...
        R(2, 1)^2);

    esSingular = valor < 1e-8;

    if ~esSingular

        anguloX = atan2( ...
            R(3, 2), ...
            R(3, 3));

        anguloY = atan2( ...
            -R(3, 1), ...
            valor);

        anguloZ = atan2( ...
            R(2, 1), ...
            R(1, 1));

    else

        anguloX = atan2( ...
            -R(2, 3), ...
            R(2, 2));

        anguloY = atan2( ...
            -R(3, 1), ...
            valor);

        anguloZ = 0;
    end

    angulos = rad2deg( ...
        [ ...
            anguloX, ...
            anguloY, ...
            anguloZ ...
        ]);
end


function evaluarParametrosFisicos( ...
    parametrosCamara, ...
    nombreCamara)

    tamano = parametrosCamara.ImageSize;

    alto = tamano(1);
    ancho = tamano(2);

    focal = parametrosCamara.FocalLength;
    centro = parametrosCamara.PrincipalPoint;
    radial = parametrosCamara.RadialDistortion;

    fprintf( ...
        'Cámara %s:\n', ...
        nombreCamara);

    if centro(1) < 0 || centro(1) > ancho

        fprintf( ...
            ['  ADVERTENCIA: Cx = %.3f px está fuera ' ...
             'del ancho de la imagen.\n'], ...
            centro(1));

    else

        fprintf( ...
            '  Cx está dentro de la imagen.\n');
    end

    if centro(2) < 0 || centro(2) > alto

        fprintf( ...
            ['  ADVERTENCIA: Cy = %.3f px está fuera ' ...
             'del alto de la imagen.\n'], ...
            centro(2));

    else

        fprintf( ...
            '  Cy está dentro de la imagen.\n');
    end

    relacionFocalImagen = ...
        max(focal) / max(alto, ancho);

    if relacionFocalImagen > 10

        fprintf( ...
            ['  ADVERTENCIA: la distancia focal supera ' ...
             'ampliamente las dimensiones de la imagen.\n']);
    end

    if any(abs(radial) > 100)

        fprintf( ...
            ['  ADVERTENCIA: existen coeficientes de ' ...
             'distorsión radial de gran magnitud.\n']);
    end

    diferenciaFocal = ...
        abs(focal(1) - focal(2)) / mean(focal);

    fprintf( ...
        '  Diferencia relativa entre Fx y Fy: %.4f %%\n', ...
        100 * diferenciaFocal);

    fprintf('\n');
end


function puntosCorregidos = corregirOrdenPuntos( ...
    puntosReferencia, ...
    puntosObjetivo, ...
    tamanoTablero)

    puntosCorregidos = puntosObjetivo;

    candidatos = cell(4, 1);

    candidatos{1} = puntosObjetivo;
    candidatos{2} = flipud(puntosObjetivo);

    try

        filas = tamanoTablero(1) - 1;
        columnas = tamanoTablero(2) - 1;

        cantidadEsperada = filas * columnas;

        if cantidadEsperada ~= size(puntosObjetivo, 1)

            error( ...
                ['La cantidad de puntos no coincide ' ...
                 'con el tamaño del tablero.']);
        end

        matrizX = reshape( ...
            puntosObjetivo(:, 1), ...
            columnas, ...
            filas)';

        matrizY = reshape( ...
            puntosObjetivo(:, 2), ...
            columnas, ...
            filas)';

        %% Inversión horizontal

        candidatoHorizontalX = fliplr(matrizX);
        candidatoHorizontalY = fliplr(matrizY);

        candidatoHorizontalXTranspuesto = ...
            candidatoHorizontalX';

        candidatoHorizontalYTranspuesto = ...
            candidatoHorizontalY';

        candidatos{3} = [ ...
            candidatoHorizontalXTranspuesto(:), ...
            candidatoHorizontalYTranspuesto(:) ...
        ];

        %% Inversión total

        candidatoTotalX = flipud( ...
            fliplr(matrizX));

        candidatoTotalY = flipud( ...
            fliplr(matrizY));

        candidatoTotalXTranspuesto = ...
            candidatoTotalX';

        candidatoTotalYTranspuesto = ...
            candidatoTotalY';

        candidatos{4} = [ ...
            candidatoTotalXTranspuesto(:), ...
            candidatoTotalYTranspuesto(:) ...
        ];

    catch ME

        warning( ...
            'No se pudieron reorganizar los puntos: %s', ...
            ME.message);

        candidatos{3} = puntosObjetivo;
        candidatos{4} = flipud(puntosObjetivo);
    end

    mejorError = Inf;

    for k = 1:numel(candidatos)

        candidato = candidatos{k};

        if isempty(candidato)
            continue;
        end

        if size(candidato, 1) ~= ...
                size(puntosReferencia, 1)

            continue;
        end

        if size(candidato, 2) ~= 2
            continue;
        end

        diferenciasVerticales = abs( ...
            puntosReferencia(:, 2) - ...
            candidato(:, 2));

        diferenciasVerticales = ...
            diferenciasVerticales( ...
                ~isnan(diferenciasVerticales));

        if isempty(diferenciasVerticales)
            continue;
        end

        errorActual = mean( ...
            diferenciasVerticales);

        if errorActual < mejorError

            mejorError = errorActual;
            puntosCorregidos = candidato;
        end
    end
end


