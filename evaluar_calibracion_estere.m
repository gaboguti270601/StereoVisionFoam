        }, ...
        'GridSize', ...
        [1 2]);
end


function guardarResumenTexto( ...
    nombreArchivo, ...
    stereoParams, ...
    errorGlobal, ...
    baseline, ...
    baselineHorizontal, ...
    angulosEuler)

    camara1 = stereoParams.CameraParameters1;
    camara2 = stereoParams.CameraParameters2;

    T = stereoParams.TranslationOfCamera2;
    R = stereoParams.RotationOfCamera2;

    fid = fopen( ...
        nombreArchivo, ...
        'w');

    if fid == -1

        warning( ...
            'No se pudo crear el archivo de resumen.');

        return;
    end

    limpiezaArchivo = onCleanup( ...
        @() fclose(fid)); %#ok<NASGU>

    fprintf( ...
        fid, ...
        'EVALUACIÓN DE CALIBRACIÓN ESTÉREO\n');

    fprintf( ...
        fid, ...
        '=================================\n\n');

    if ~isnan(errorGlobal)

        fprintf( ...
            fid, ...
            'Error medio de reproyección: %.6f px\n', ...
            errorGlobal);

    else

        fprintf( ...
            fid, ...
            'Error medio de reproyección: no disponible\n');
    end

    fprintf( ...
        fid, ...
        'Baseline total: %.6f unidades\n', ...
        baseline);

    fprintf( ...
        fid, ...
        'Componente horizontal |Tx|: %.6f unidades\n\n', ...
        baselineHorizontal);

    fprintf( ...
        fid, ...
        'VECTOR DE TRASLACIÓN\n');

    fprintf( ...
        fid, ...
        'Tx = %.10f\n', ...
        T(1));

    fprintf( ...
        fid, ...
        'Ty = %.10f\n', ...
        T(2));

    fprintf( ...
        fid, ...
        'Tz = %.10f\n\n', ...
        T(3));

    fprintf( ...
        fid, ...
        'ÁNGULOS DE ROTACIÓN APROXIMADOS\n');

    fprintf( ...
        fid, ...
        'Rotación X = %.10f grados\n', ...
        angulosEuler(1));

    fprintf( ...
        fid, ...
        'Rotación Y = %.10f grados\n', ...
        angulosEuler(2));

    fprintf( ...
        fid, ...
        'Rotación Z = %.10f grados\n\n', ...
        angulosEuler(3));

    fprintf( ...
        fid, ...
        'MATRIZ DE ROTACIÓN\n');

    for fila = 1:3

        fprintf( ...
            fid, ...
            '%.10f  %.10f  %.10f\n', ...
            R(fila, 1), ...
            R(fila, 2), ...
            R(fila, 3));
    end

    fprintf( ...
        fid, ...
        '\nCÁMARA IZQUIERDA\n');

    escribirCamaraEnArchivo( ...
        fid, ...
        camara1);

    fprintf( ...
        fid, ...
        '\nCÁMARA DERECHA\n');

    escribirCamaraEnArchivo( ...
        fid, ...
        camara2);
end


